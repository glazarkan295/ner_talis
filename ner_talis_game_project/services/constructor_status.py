"""Общий жизненный цикл статусов для EntityStore-конструкторов (единый стандарт).

Конструкторы переиспользуют один набор статусов/переходов/подписей, чтобы не
дублировать их в каждом сервисе. Импортируется через ``from ... import *``.
"""

