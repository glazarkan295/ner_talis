export function ReferenceSection() {
  const [query, setQuery] = useState("");
  const q = query.trim().toLowerCase();
  const groups = useMemo(() => GROUPS.filter(([, map]) => map && typeof map === "object"), []);
  const match = (code, name) => !q || String(code).toLowerCase().includes(q) || String(name).toLowerCase().includes(q);

  return (
    <section className="ntv2-section">
      <h2>Справочник</h2>
      <div className="ntv2-filters">
        <input placeholder="Поиск по коду или названию" value={query} onChange={(e) => setQuery(e.target.value)} />
      </div>

      {groups.map(([title, map]) => {
        const rows = Object.entries(map).filter(([code, name]) => match(code, name));
        if (!rows.length) return null;
        return (
          <details className="ntv2-tech" key={title} open={Boolean(q)}>
            <summary>{title} ({rows.length})</summary>
            <div className="ntv2-list">
              {rows.map(([code, name]) => (
                <div className="ntv2-list-row" key={code}>
                  <b>{name}</b>
                  <span className="ntv2-perm-chip">{code}</span>
                </div>
              ))}
            </div>
          </details>
        );
      })}

      <h3>Краткие гайды</h3>
      <div className="ntv2-list">
        {GUIDES.filter(([t, b]) => match(t, b)).map(([title, body]) => (
          <details className="ntv2-tech" key={title} open={Boolean(q)}>
            <summary>{title}</summary>
            <p className="ntv2-hint" style={{ margin: "6px 0" }}>{body}</p>
          </details>
        ))}
      </div>
    </section>
  );
}
